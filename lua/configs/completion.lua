  require("copilot.suggestion").toggle_auto_trigger()
end

-- Map <Leader>tc to the toggle function
vim.api.nvim_set_keymap('n', '<Leader>tc', ':lua ToggleCopilotAutoTrigger()<CR>', { noremap = true, silent = true })
